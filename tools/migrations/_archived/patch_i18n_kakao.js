function injectLocales(filePath, dictObj, keyNs) {
    let content = fs.readFileSync(filePath, 'utf8');
    
    if (content.includes(`${keyNs}:`)) return; // Already exists
    
    const replacer = `  ${keyNs}: ${JSON.stringify(dictObj, null, 4).replace(/\n/g, '\n  ')},\n});\n\nexport default`;
    content = content.replace(/\}\);\s*export default/, replacer);
    
    fs.writeFileSync(filePath, content, 'utf8');
}

