function patchFile(filepath, dictName, dictObj) {
    let content = fs.readFileSync(filepath, 'utf8');
    
    // Find where the default export object begins, or where to inject
    // The locale file format is `export default { ... }`
    // We will inject `channelKpi: { ... },` right after the first open brace of export default
    
    if (content.includes('channelKpi: {')) {
        console.log(filepath + " already contains channelKpi!");
        // We will replace it using regex
        const re = /channelKpi:\s*\{[\s\S]*?\}(,)?/;
        content = content.replace(re, "channelKpi: " + JSON.stringify(dictObj, null, 4) + "$1");
    } else {
        const insertMatch = /const (ko|en) = \{/;
        content = content.replace(insertMatch, "const $1 = {\n  channelKpi: " + JSON.stringify(dictObj, null, 4) + ",");
    }

    fs.writeFileSync(filepath, content);
    console.log("Successfully patched " + filepath);
}

