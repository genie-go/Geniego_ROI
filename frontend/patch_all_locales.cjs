function patchLocale(lang, translations) {
  const filePath = path.join(LOCALES_DIR, `${lang}.js`);
  if (!fs.existsSync(filePath)) {
    console.log(`⚠️ ${lang}.js not found, skipping`);
    return;
  }
  
  let content = fs.readFileSync(filePath, 'utf8');
  
  // Check if marketing section exists
  const marketingIdx = content.indexOf('"marketing"');
  const marketingIdx2 = content.indexOf("'marketing'");
  
  if (marketingIdx >= 0 || marketingIdx2 >= 0) {
    // Marketing section exists — add missing keys
    const idx = marketingIdx >= 0 ? marketingIdx : marketingIdx2;
    const braceStart = content.indexOf('{', idx + 10);
    if (braceStart >= 0) {
      const insertKvs = Object.entries(translations)
        .map(([k, v]) => `"${k}":${JSON.stringify(v)}`)
        .join(',');
      content = content.slice(0, braceStart + 1) + insertKvs + ',' + content.slice(braceStart + 1);
    }
  } else {
    // Marketing section doesn't exist — create it
    // Find the last } before export default } or the end
    // Look for the pattern: ,"someSection":{ or the end of the default export object
    const exportMatch = content.lastIndexOf('};');
    if (exportMatch >= 0) {
      // Insert before the closing };
      const lastBrace = content.lastIndexOf('}', exportMatch - 1);
      if (lastBrace >= 0) {
        const marketingSection = ',"marketing":{' + 
          Object.entries(translations).map(([k, v]) => `"${k}":${JSON.stringify(v)}`).join(',') + 
          '}';
        content = content.slice(0, lastBrace + 1) + marketingSection + content.slice(lastBrace + 1);
      }
    }
  }
  
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`✅ ${lang}.js patched with ${Object.keys(translations).length} keys`);
}

// Patch all locales
for (const [lang, translations] of Object.entries(TRANSLATIONS)) {
  patchLocale(lang, translations);
}

