// 정렬: 긴 키 먼저 매칭 (부분 매칭 우선순위)
const SORTED_KEYS = Object.keys(DICT).sort((a, b) => b.length - a.length);

function tryMap(jpText) {
  if (!jpText || jpText.trim() === '') return null;
  
  // 1. 완전 일치 우선
  if (DICT[jpText]) return DICT[jpText];
  
  // 2. trim 후 일치
  const trimmed = jpText.trim();
  if (DICT[trimmed]) return DICT[trimmed];
  
  return null;
}

// ───── CSV 파싱 ─────
function parseCsv(text) {
  // BOM 제거
  if (text.charCodeAt(0) === 0xFEFF) text = text.slice(1);
  const lines = [];
  let cur = '';
  let inQuote = false;
  const rows = [];
  let row = [];
  
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuote) {
      if (c === '"') {
        if (text[i + 1] === '"') {
          cur += '"';
          i++;
        } else {
          inQuote = false;
        }
      } else {
        cur += c;
      }
    } else {
      if (c === '"') inQuote = true;
      else if (c === ',') {
        row.push(cur);
        cur = '';
      } else if (c === '\n') {
        row.push(cur);
        rows.push(row);
        row = [];
        cur = '';
      } else if (c === '\r') {
        // skip
      } else {
        cur += c;
      }
    }
  }
  if (cur !== '' || row.length > 0) {
    row.push(cur);
    rows.push(row);
  }
  return rows;
}

function csvCell(v) {
  if (v === null || v === undefined) return '';
  const s = String(v);
  if (s.includes(',') || s.includes('"') || s.includes('\n') || s.includes('\r')) {
    return `"${s.replace(/"/g, '""')}"`;
  }
  return s;
}

// ───── main ─────
(() => {
  console.log(`[B3] reading workbook: ${IN_CSV}`);
  const text = fs.readFileSync(IN_CSV, 'utf8');
  const rows = parseCsv(text);
  const header = rows[0];
  const body = rows.slice(1).filter(r => r.length >= 4 && r[0]);
  
  console.log(`[B3] total rows : ${body.length}`);
  console.log(`[B3] dict size  : ${SORTED_KEYS.length}`);
  
  const mapped = [];
  const unmapped = [];
  
  for (const row of body) {
    const [p, ko_current, ja_value, proposed_ko, ns, type] = row;
    const ko = tryMap(ko_current); // ko_current가 일본어로 오염된 값
    if (ko) {
      mapped.push([p, ko_current, ja_value, ko, ns, type]);
    } else {
      unmapped.push([p, ko_current, ja_value, '', ns, type]);
    }
  }
  
  // 출력
  const headerLine = header.map(csvCell).join(',');
  fs.writeFileSync(
    OUT_MAPPED,
    '\uFEFF' + [headerLine, ...mapped.map(r => r.map(csvCell).join(','))].join('\n'),
    'utf8'
  );
  fs.writeFileSync(
    OUT_UNMAPPED,
    '\uFEFF' + [headerLine, ...unmapped.map(r => r.map(csvCell).join(','))].join('\n'),
    'utf8'
  );
  
  // 미매핑 빈도 분석
  const freq = {};
  for (const row of unmapped) {
    const v = row[1];
    freq[v] = (freq[v] || 0) + 1;
  }
  const topUnmapped = Object.entries(freq).sort((a, b) => b[1] - a[1]).slice(0, 30);
  
  console.log('\n========== 147 B3 SUMMARY ==========');
  console.log(`auto mapped     : ${mapped.length} (${(mapped.length / body.length * 100).toFixed(1)}%)`);
  console.log(`unmapped        : ${unmapped.length}`);
  console.log('\nTop 30 unmapped values (for next dict expansion):');
  topUnmapped.forEach(([v, n], i) => {
    const short = v.length > 50 ? v.slice(0, 50) + '...' : v;
    console.log(`  ${String(i + 1).padStart(2)}. [${n}x] ${short}`);
  });
  console.log(`\n[OUT] ${OUT_MAPPED}`);
  console.log(`[OUT] ${OUT_UNMAPPED}`);
  console.log('====================================');
})();
