// ── Step 1: Inject auth keys into existing locale files ──
const existingLocales = ['ko','en','ja','zh','zh-TW','de','es','fr','th','vi','id'];
for (const lang of existingLocales) {
  const filePath = path.join(LOCALES_DIR, lang === 'zh-TW' ? 'zh-TW.js' : `${lang}.js`);
  if (!fs.existsSync(filePath)) { console.log(`Skip ${lang}: file not found`); continue; }
  
  let code = fs.readFileSync(filePath, 'utf8');
  // Check if auth key already exists
  if (code.includes('"auth":{') || code.includes('"auth":')) {
    console.log(`${lang}: auth keys already exist, skipping`);
    continue;
  }
  
  // Find the opening { after "export default" and inject auth
  const authJson = JSON.stringify({ auth: AUTH[lang] });
  // Remove outer { } to merge into existing object
  const authInner = authJson.slice(1, -1); // "auth":{...}
  
  // Insert after the first {
  const insertPos = code.indexOf('{');
  if (insertPos >= 0) {
    code = code.slice(0, insertPos + 1) + authInner + ',' + code.slice(insertPos + 1);
    fs.writeFileSync(filePath, code, 'utf8');
    console.log(`✅ ${lang}: auth keys injected`);
  }
}

// ── Step 2: Create NEW locale files (pt, ru, ar, hi) ──
const newLocales = { pt: 'pt', ru: 'ru', ar: 'ar', hi: 'hi' };
for (const [lang, filename] of Object.entries(newLocales)) {
  const filePath = path.join(LOCALES_DIR, `${filename}.js`);
  // Create a minimal locale file with auth keys (using English fallback for non-auth keys)
  const localeData = { auth: AUTH[lang] };
  if (lang === 'ar') localeData.dir = 'rtl';
  const code = `export default ${JSON.stringify(localeData)};\n`;
  fs.writeFileSync(filePath, code, 'utf8');
  console.log(`✅ ${lang}: new locale file created`);
}

