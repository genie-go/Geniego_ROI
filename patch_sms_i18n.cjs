// Process each locale file
const LANGS = ['ko', 'en', 'ja', 'zh', 'zh-TW', 'de', 'th', 'vi', 'id'];

LANGS.forEach(lang => {
  const filePath = path.join(LOCALES_DIR, `${lang}.js`);
  let content = fs.readFileSync(filePath, 'utf8');

  const keysToAdd = NEW_KEYS[lang];
  if (!keysToAdd) {
    console.log(`SKIP ${lang}: no new keys defined`);
    return;
  }

  // Find the existing sms object and merge new keys into it
  // The locale files are single-line JS like: export default {"key":"val",...,"sms":{...},...};
  // We need to find "sms":{...} and add our new keys

  const smsRegex = /"sms"\s*:\s*\{/;
  const match = smsRegex.exec(content);
  if (!match) {
    console.log(`SKIP ${lang}: no sms namespace found`);
    return;
  }

  // Find the matching closing brace for the sms object
  let depth = 0;
  let startIdx = match.index + match[0].length - 1; // position of opening {
  let endIdx = startIdx;
  for (let i = startIdx; i < content.length; i++) {
    if (content[i] === '{') depth++;
    if (content[i] === '}') depth--;
    if (depth === 0) {
      endIdx = i;
      break;
    }
  }

  // Extract existing sms content
  const existingSmsStr = content.substring(startIdx, endIdx + 1);
  
  // Build new keys string
  const newKeysStr = Object.entries(keysToAdd)
    .map(([k, v]) => `"${k}":"${v.replace(/"/g, '\\"')}"`)
    .join(',');

  // Insert new keys before the closing brace
  const updatedSms = existingSmsStr.slice(0, -1) + ',' + newKeysStr + '}';
  
  // Replace in content
  content = content.substring(0, startIdx) + updatedSms + content.substring(endIdx + 1);

  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`✅ ${lang}: ${Object.keys(keysToAdd).length} keys added`);
});

